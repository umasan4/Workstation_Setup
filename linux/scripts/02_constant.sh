# 定数の宣言ルール ------------------------------
# 1. 大文字で記述する
# 2. 取出し方は変数と同じ
# 3. 値の変更, 削除はエラーになる
#-----------------------------------------------

# 構文: readonly {定数名}

# 宣言 -----------------------------------------
readonly CONSTANT="hoge is constant"

# 呼出し ---------------------------------------
echo "${CONSTANT}"